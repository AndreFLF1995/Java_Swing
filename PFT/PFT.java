    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try
        {
            FileOutputStream File = new FileOutputStream("Transactions.csv"); //path

            OutputStreamWriter write = new OutputStreamWriter(File);

            write.write(TM.transactionFile());

            write.close();

            JOptionPane.showMessageDialog(null, "Csv file created successfully!", "Save", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error in the creation of the csv file.", "Save", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void importButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButtonActionPerformed
        TM.transactionList.clear();

        BufferedReader Read;

        try
        {
            Read = new BufferedReader(new FileReader("Transactions.csv")); //path

            String line = Read.readLine();

            while(line!=null)
            {
                String[] V = line.split(";");

                Transaction T = new Transaction(V[1],Double.parseDouble(V[2]),V[3],V[4]);

                TM.addTransaction(T);
                line = Read.readLine();
            }


            JOptionPane.showMessageDialog(null, "Csv file imported successfully!", "Import", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(null, "Error importing the csv file.", "Import", JOptionPane.ERROR_MESSAGE);
        }
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        for (Transaction T : TM.transactionList) {
            Object[] rowData = {
                false, // Checkbox column
                T.getId(),
                T.getAmount(),
                T.getDate(),
                T.getCategory(),
                T.getDescription()
            };
            model.addRow(rowData);
        }

        // Update the total label
        txtTotal.setText(Double.toString(TM.calculateTotalExpenses()) + "€");
    }//GEN-LAST:event_importButtonActionPerformed

