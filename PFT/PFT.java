    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        double amount;
        String category = comboCategory.getSelectedItem().toString();
        String description = txtDescription.getText();
        
        String date = txtDate.getText();
        String datePattern = "^(0[1-9]|[12][0-9]|3[01])-(0[1-9]|1[0-2])-(\\d{4})$";
        
        if (!date.matches(datePattern)) 
        {
            JOptionPane.showMessageDialog(null, "Please enter a valid date in dd-mm-yyyy format.", "Invalid Date", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            amount = Double.parseDouble(txtAmount.getText());
            if (amount <= 0) {
                JOptionPane.showMessageDialog(null, "Please enter a positive amount.", "Invalid Amount", JOptionPane.ERROR_MESSAGE);
                return; // Stop execution if the amount is zero or negative
            }
        } 
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter a valid numeric amount.", "Invalid Amount", JOptionPane.ERROR_MESSAGE);
            return; // Stop execution if the amount is not a valid number
        }
        
        Transaction T = new Transaction(date, amount, category, description);
        
        TM.addTransaction(T);
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        
    // Add each transaction as a row
        for (Transaction Tr : TM.transactionList)
        {
            Object[] rowData = {
                false, // Checkbox column
                Tr.getId(), // Assuming Transaction has an Id field
                Tr.getAmount(),
                Tr.getDate(),
                Tr.getCategory(),
                Tr.getDescription()
            };
            model.addRow(rowData);
        }
        txtTotal.setText(Double.toString(TM.calculateTotalExpenses())+"€");
        
        JOptionPane.showMessageDialog(null, "Record Inserted", "Add Transaction", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_addButtonActionPerformed

