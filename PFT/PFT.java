    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    
        if (selectedRow != -1) { // Ensure a row is selected
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Retrieve the Id from the selected row
            int id = (int) model.getValueAt(selectedRow, 1); // Assuming the Id is in the second column

            // Remove the transaction by Id
            TM.removeTransaction(id);

            // Clear and refresh the table
            model.setRowCount(0);
            for (Transaction T : TM.transactionList) {
                Object[] rowData = {
                    false, // Checkbox column
                    T.getId(),
                    T.getAmount(),
                    T.getDate(),
                    T.getCategory(),
                    T.getDescription()
                };
                model.addRow(rowData);
            }

            // Update the total label
            txtTotal.setText(Double.toString(TM.calculateTotalExpenses()) + "€");
        } 
        else 
        {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.", "Delete Transaction", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

